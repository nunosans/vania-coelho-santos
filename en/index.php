			<h2>Cinematheque</h2>
			<div class="col2 right">
			    <p>This competition is based on a particularity: the challenge was presented and the teams only had 48h to present a solution.
The site represents the typical topography of Oporto downhill, extending to the river and providing a magnificent view of the bridge Luís I and the Fernandina historic Wall, - remarkable icons that conceives to the location an emblematic feeling.</p>
				<p>The proposal should develop an intense relationship with the sumptuous location, recognising the local environment and toasting to the city a space for the movie lovers.</p>
				<p>How could this relationship be developed? The concept was base on the reinterpretation of the Obscure Camara logic (the principal that led to the first camera): the whole building revolves around the idea of transition to a new atmosphere of introspection, silence and isolation as if it were a blackroom and Oporto’s landscape is captured through a pinhole camera, projecting it in the wall.</p>
			</div>
