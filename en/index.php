		<div class="project" id="vienna-house-of-music">
			<h2 class="col2">Vienna House of Music</h2>
			<div class="col2 right">
				<p>Music is intuitively recognised by any one of us. It’s a result of a manipulation of sounds and silences in delicate and meticulous sequences over a period of time, it is, by excellence one of the noblest expressions of the human culture. Rhythm, harmony, fluidity, are characteristics intrinsically connected to music and that arise thoroughly incorporated into this proposal.</p>
				<p>The fluidity of the spaces, the set of curvilinear shapes that deviate and / or join, the search for harmony between interior / exterior, between form and function, full and empty, denounces the primary intention of overfill the characteristics of the object - Music - to the space that houses - the house of music in Vienna. Since this is the city by excellence of classical music, this new building will be, without  no margin of doubt, a reference of relevant importance at this level.</p>
				<p>In regard to the implementation of the building, it was tempted that the building created a dialogue with both the city as planned and orthogonal as the city’s  clear, open and natural spaces – the garden.</p>
 			</div>
			<img class="col2 right bmargin" src="http://media.vaniacoelhosantos.com/vienna-house-of-music-concept.png" width="975" height="311" alt="A sketch of the main idea for the Vienna House of Music building">
