			<h2  class="col2">Shelter for Pilgrims</h2>
			<div class="col2 right">
			    <p>The main focus of this proposal was to design a space whose characteristics solved the pilgrims physical and spiritual necessities such as hygiene and rest and reflection and introspection.</p>
				<p>The sea shell, emblem of the pilgrims, inspired the form of this building creating a reunion space at its core which branches out into several individual intimate spaces for meditation. The Shelter’s identity is made mainly from ins and outs created by the Pladour boards which structure and protect it. These boards with equal gaps cast stripes of light agains the interior walls producing the required transcendent atmosphere.</p>
