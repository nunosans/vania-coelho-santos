			<h2>Collective Housing</h2>
			<div class="col2 right">
			    <p>This work was developed in three phases / moments, each one with specific purposes.</p>
				<p><b>Moment 1:</b> <i>The City</i></p>
				<p>The site to intervene is located in a residential area near to a strong centrality - the Boavista Rotunda. It’s dimensions demanded the designing of a new street allowed the access to the new blocks. All the areas for commerce and offices activity are located on streets with higher traffic, and with connection to important areas of the city - the street of Graciosa and Street of Quinta Amarela.</p>
				<p><b>Moment 2:</b> <i>The Building</i></p>
				<p>The second moment was the definition of a housing block. The direct connection, of the new block, with a preexistent building, created the necessity of develop a continuity between the two facades. The new house building result from the intersection of two bodies: one that gives continuity to the preexistent building and defines the balconies apartments, and, another one - the principal -, which contains de habitations.</p>
			</div>
			<a href="http://media.vaniacoelhosantos.com/collective-housing-at-night-hires.jpg"><img class="col4" src="http://media.vaniacoelhosantos.com/collective-housing-at-night.jpg" width="1945" height="1300" alt="A computer generated image illustrating the Collective Housing as seen from the outside at night"></a>
